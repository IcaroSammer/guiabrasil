var expanded = false;

function showCheckboxeseEnviroment() {
  var checkboxes = document.getElementById("enviroment");
  if (!expanded) {
    checkboxes.style.display = "block";
    expanded = true;
  } else {
    checkboxes.style.display = "none";
    expanded = false;
  }
}
